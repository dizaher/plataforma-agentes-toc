			<h3>Financieras, Incubadoras y Aceleradoras</h3>
			<p><strong>Empresa financiera</strong> se denomina a la persona natural o a la persona jurídica que se dedique a ofrecer al público préstamos o facilidades de financiamiento en dinero para el desarrollo de proyectos de investigación, desarrollo tecnológico, nuevos productos o procesos</p>
			<p><strong>Incubadora de empresas</strong> es una organización diseñada para acelerar el crecimiento y asegurar el éxito de proyectos emprendedores a través de una amplia gama de recursos y servicios empresariales que puede incluir renta de espacios físicos, capitalización, coaching networking (es decir acceso a una red de contactos) y otros servicios básicos como telecomunicaciones, limpieza o estacionamiento.</p>
			<p>Las incubadoras de empresas usualmente son patrocinadas, apoyadas y operadas por compañías privadas, entidades gubernamentales o universidades. Su propósito primordial es ayudar a crear y crecer empresas jóvenes proveyéndolas con el apoyo necesario de servicios técnicos y financieros.</p>			
			<hr>
			<p><strong>¿Qué es una incubadora de negocios?</strong>Son centros de apoyo que te ayudan a montar tu empresa. Las incubadoras se encargan de evaluar la viabilidad técnica, financiera y de mercado de tu proyecto empresarial, asi también te proporcionan servicios integrales de asesoría legal y administrativa, así como planes de mercadotecnia y ventas, e incluso espacio físico, equipo, logística y hasta acceso a financiamiento y capital semilla.</p>
			<p><strong>Tipos de incubadora de negocios</strong></p>
			<p>Existen incubadoras privadas, universitarias y gubernamentales, toda ellas avaladas por la Secretaría de Economía.</p>
			<p>Cada tipo de incubadora tiene diferentes estructuras y cada una tiene enfoques específicos.</p>
			<p><strong>Tipos de incubadoras por giro:</strong></p>
			<p><strong>Incubadoras tradicionales: </strong>se enfocan al comercio, servicios y algunas industrias ligeras; por ejemplo, papelerías, confección y maquila de ropa, loncherías, farmacias, etcétera. </p>
			<p>Prácticamente no necesitan infraestructura tecnológica, su implantación es más sencilla y el tiempo de incubación promedio es de hasta tres meses.</p>
			<p>Las incubadoras tradicionales por lo general, no brindan dinero en efectivo sino las herramientas necesarias para arrancar su empresa y posteriormente le pueden ayudar a obtener el capital de riesgo para continuar su desarrollo fuera de la incubadora.</p>
			<p><strong>Incubadoras de tecnología intermedia: </strong>las empresas incubadas bajo este diseño incorporan elementos de innovación por lo que deben vincularse a centros e institutos de conocimiento, fuentes generadoras de proyectos, grandes empresas o redes estratégicas de innovación. Ejemplos de este tipo de incubadoras son el desarrollo de software para el control de inventarios, servicios basados en tecnologías de la información y diseño, desarrollo de tintes y colorantes especiales, nuevas máquinas y herramientas para la industria, etc.</p>
			<p>Emplean un tiempo máximo de incubación de 12 meses.</p>
			<p><strong>Incubadoras de Alta Tecnología:</strong>en este tipo de incubadoras se incluye a los negocios enfocados a los sectores de software especializado, computación, consultoría en Tecnologías de la Información y Comunicaciones (TIC), multimedia, diseño de microprocesadores, biotecnología y otros.</p>
			<p>Las incubadoras de alta tecnología requieren de mayor tiempo de incubación, amplia infraestructura física y tecnológica y operación altamente especializada.</p>
			<p>En nuestro país existen 25 modelos de incubación aprobados por el Consejo Nacional de Incubadoras. De las 209 incubadoras de negocios que operan 67 son tradicionales, 132 son de tecnología intermedia y 10 de alta tecnología.</p>
